package domain;

public class GeoInfo {

    private String type;
    private String coordinates;



    public String getType() {
        return type;
    }

    public String getCoordinates() {
        return coordinates;
    }

    public void setCoordinates(String coordinates) {
        this.coordinates = coordinates;
    }

    public void setType(String type) {
        this.type = type;
    }
}




